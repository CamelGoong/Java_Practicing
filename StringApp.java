
public class StringApp {

	public static void main(String[] args) {
		
		System.out.println("Hello World"); // String(character들이 모여있는 데이터타입) 큰따옴표
		System.out.println('H'); // Character(한글자를 표현하는 데이터타입) 작은따옴표
		System.out.println("H"); // 이건 큰따옴표니까 String
		
		// \n: new line
		System.out.println("Hello \nWorld");
		
		System.out.println("Hello \"World\""); // Hello "World" 역슬래쉬 다음에 보이고 싶은 문자기호를 넣으면 그렇게 처리가 됨
	}

}
